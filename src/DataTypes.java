public class DataTypes 
{
	public static void main(String args[])
	{
		System.out.print("Hello") ;
		// Introduction 
        System.out.print("Hello World!") ;   // No newline
        System.out.println("Hello World!") ; // newline 
        String k ;
        k = "Java" ;
        System.out.println(k);


        /*Data types */
        
        //To Store a number 
        byte a = 4  ;  //1 bytes
        short b = 4 ;  //2 bytes
        int c = 5   ;  //4 bytes  ** mostly used 
        long d = 6  ;  //8 bytes  ** mostly used 

        //To Store floating point 
        float q = 6.3f  ; 
        double z = 8.7 ;//  double precision of float  ** mostly used 

        char x = 'a' ;// To store a single character 
        String y = "Hello world" ;// To store string , Always use " "

        boolean g = true ;// To store boolean 
         
        /*Type conversion */
	}
}